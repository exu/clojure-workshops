;; Higher Order Functions
;; A higher order function is a function that either:

;; Takes one or more functions as arguments

;; Returns a function as its result

;; This is an important concept in functional programming in any
;; language.

;; Higher order functions allow us to compose functions. This means we
;; can write small functions and combine them to create larger
;; functions. Like putting a bunch of small LEGO bricks together to
;; build a house.
